package src.Lab1_4;

/*
Write a console program prints on console each of the primitive data types (byte, short,
int, long, float, double, char, boolean).
*/

public class Lab41 {
    public static void main(String args[]) {
        byte c = 127;
        short a = 32767;
        int s = 2147483647;
        long d = 65443515313535L;
        float g = 251.257F;
        double h = 2536.153D;
        char q = 'u';
        boolean w = false;

        System.out.println(c);
        System.out.println(a);
        System.out.println(s);
        System.out.println(d);
        System.out.println(g);
        System.out.println(h);
        System.out.println(q);
        System.out.println(w);
    }
}
